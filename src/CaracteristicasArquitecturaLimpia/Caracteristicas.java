package CaracteristicasArquitecturaLimpia;

public class Caracteristicas {

    /**
     *
     * Características principales del 'Arquitectura limpia':
     * - Estructura de forma comprensible, siendo fácil de leer.
     * - Es claro en sus intenciones, nombrando clases y funciones por nombres fácilmente reconocibles.
     * - Es fácilmente mantenible ante cambios que se deban realizar con el tiempo.
     * - Los componentes deben de poder ser desplegados de forma independiente
     *   sin afectar a los demás.
     * - Agrupar clases que puedan cambiar por la misma razón en un solo componente.
     * - El componente que dependan de otro, será porque necesita todas las clases que la componen.
     * - Testable.
     * - Independiente de la base de datos.
     * - Independiente de cualquier entidad externa.
     *
     */

    /**
     *
     * La arquitectura limpia se debe utilizar siempre que sea posible, sobre todo en proyectos
     * de gran magnitud, porque a la hora de su mantenimiento e ir actualizándose con el tiempo
     * todo sera mucho mas fácil para cualquier programador y mas sostenible sin necesidad de tener
     * que cambiar muchas lineas de código.
     * Es recomendable a la hora de empezar un proyecto ir aplicando esta serie de normas siempre
     * que sea posible.
     */

}
